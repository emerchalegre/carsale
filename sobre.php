	<h1 id="sobretitle">Sobre a Carsale</h1>
	<p>A Car Sale é especializada na comercialização de veículos de coleção, das melhores e mais tradicionais marcas mundiais.</p>
	<p>Trabalhando seriamente com o objetivo de oferecer ao mercado produtos diferenciados, a Car Sale seleciona seus carros através de um rigoroso critério técnico de avaliação, garantindo sua qualidade e procedência. Além disso, oferece garantia de três meses para motor e câmbio para todos os veículos.</p>
	<p>Atuamos de maneira objetiva e eficaz. Aceitamos seu veículo usado como parte do pagamento e fazemos a melhor avaliação possível, conforme as condições do veículo e dentro das normas do mercado.</p>
	<p>A Car Sale faz questão de encontrar o carro dos seus sonhos e, para isso, presta um atendimento personalizado. Atuando de maneira objetiva, mantemos diversas opções ao seu dispor ou ainda procuramos o veículo que você deseja, sem compromisso.</p>

